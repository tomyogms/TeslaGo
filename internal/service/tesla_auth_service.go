// refreshAndPersistToken calls the Tesla refresh endpoint, encrypts the new
// tokens, and saves them to the database. It returns the updated TeslaUser.
//
// Note: Tesla rotates the refresh token on every refresh — the old one becomes
// invalid. Always save the NEW refresh token immediately.
func (s *teslaAuthService) refreshAndPersistToken(ctx context.Context, teslaUser *model.TeslaUser) (*model.TeslaUser, error) {
	// Decrypt the stored refresh token so we can send it to Tesla.
	plainRefreshToken, err := extTesla.Decrypt(teslaUser.RefreshToken, s.tokenSecret)
	if err != nil {
		return nil, fmt.Errorf("decrypting refresh token: %w", err)
	}

	// Call Tesla's token endpoint with grant_type=refresh_token.
	tokenResp, err := s.teslaClient.RefreshToken(s.authBaseURL, s.clientID, plainRefreshToken)
	if err != nil {
		return nil, fmt.Errorf("calling tesla refresh: %w", err)
	}

	// Encrypt the brand-new tokens before saving.
	encAccessToken, err := extTesla.Encrypt(tokenResp.AccessToken, s.tokenSecret)
	if err != nil {
		return nil, fmt.Errorf("encrypting new access token: %w", err)
	}

	encRefreshToken, err := extTesla.Encrypt(tokenResp.RefreshToken, s.tokenSecret)
	if err != nil {
		return nil, fmt.Errorf("encrypting new refresh token: %w", err)
	}

	// Update the in-memory struct and persist to DB.
	teslaUser.AccessToken = encAccessToken
	teslaUser.RefreshToken = encRefreshToken
	teslaUser.TokenExpiresAt = time.Now().UTC().Add(time.Duration(tokenResp.ExpiresIn) * time.Second)

	if err := s.repo.UpsertTeslaUser(ctx, teslaUser); err != nil {
		return nil, fmt.Errorf("saving refreshed tesla user: %w", err)
	}

	return teslaUser, nil
}

// syncVehicles fetches the list of vehicles from the Tesla API and upserts
// each one into our database.
//
// This is called right after a successful OAuth callback while we still have
// the plain-text access token available. It is best-effort — errors are
// returned but the caller (HandleCallback) ignores them.
func (s *teslaAuthService) syncVehicles(ctx context.Context, teslaUser *model.TeslaUser, plainAccessToken string) error {
	// Ask Tesla for all vehicles on this account.
	vehicles, err := s.teslaClient.GetVehicles(plainAccessToken)
	if err != nil {
		return fmt.Errorf("fetching vehicles from tesla: %w", err)
	}

	// Save each vehicle, creating a new row or updating the existing one.
	for _, v := range vehicles {
		vehicle := &model.TeslaVehicle{
			TeslaUserID: teslaUser.ID,
			VehicleID:   v.ID, // Tesla's Owner API ID (use for API calls)
			DisplayName: v.DisplayName,
			VIN:         v.VIN,
			State:       v.State, // online / asleep / offline
		}
		if err := s.repo.UpsertTeslaVehicle(ctx, vehicle); err != nil {
			return fmt.Errorf("upserting vehicle %d: %w", v.ID, err)
		}
	}

	return nil
}
